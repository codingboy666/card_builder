    // Function to generate sequential greeting for a holiday
    function generateSequentialGreeting(holiday) {
        const templates = greetingTemplates[holiday];
        if (!templates) {
            return '祝你节日快乐！';
        }
        
        // Initialize the index tracker for this holiday if it doesn't exist
        if (lastGreetingIndex[holiday] === undefined) {
            lastGreetingIndex[holiday] = -1;
        }
        
        // Increment the index and wrap around if needed
        lastGreetingIndex[holiday] = (lastGreetingIndex[holiday] + 1) % templates.length;
        
        // Return the greeting at the current index
        return templates[lastGreetingIndex[holiday]];
    }

