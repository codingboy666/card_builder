    // 背景音乐控制
    function initBackgroundMusic() {
        const musicToggle = document.getElementById('music-toggle');
        const backgroundMusic = document.getElementById('background-music');
        const vinylPlayer = document.querySelector('.vinyl-player');
        
        if (!musicToggle || !backgroundMusic) return;
        
        // 设置音量较低以增加自动播放成功率
        backgroundMusic.volume = 0.4;
        
        // 更新唱片机状态
        const updatePlayerState = (isPlaying) => {
            if (isPlaying && !backgroundMusic.muted) {
                vinylPlayer.classList.add('playing');
            } else {
                vinylPlayer.classList.remove('playing');
            }
        };
        
        // 超级增强的自动播放尝试函数
        const attemptPlayMethods = () => {
            console.log('尝试播放背景音乐...');
            
            // 尝试解锁音频上下文（对一些浏览器有效）
            try {
                const AudioContext = window.AudioContext || window.webkitAudioContext;
                if (AudioContext) {
                    const audioCtx = new AudioContext();
                    if (audioCtx.state === 'suspended') {
                        audioCtx.resume();
                    }
                }
            } catch (e) {
                console.error('无法解锁音频上下文', e);
            }
            
            // 尝试不同的播放方法
            try {
                // 1. 直接尝试播放
                backgroundMusic.volume = 0.4;
                backgroundMusic.muted = false;
                const playPromise = backgroundMusic.play();
                
                if (playPromise !== undefined) {
                    playPromise.then(() => {
                        // 自动播放成功
                        console.log('背景音乐自动播放成功！');
                        updatePlayerState(true);
                        
                        // 确保播放器状态正确
                        setTimeout(() => {
                            if (!backgroundMusic.paused) {
                                updatePlayerState(true);
                            }
                        }, 500);
                    }).catch(error => {
                        console.log('常规自动播放失败，尝试静音播放:', error);
                        
                        // 2. 尝试静音播放
                        backgroundMusic.muted = true;
                        backgroundMusic.play().then(() => {
                            console.log('静音自动播放成功，等待用户交互后解除静音');
                            updatePlayerState(false);
                            
                            // 添加一次性事件监听器，等待用户交互后取消静音
                            const enableAudio = () => {
                                backgroundMusic.muted = false;
                                updatePlayerState(true);
                                document.removeEventListener('click', enableAudio);
                                document.removeEventListener('touchstart', enableAudio);
                                document.removeEventListener('keydown', enableAudio);
                            };
                            
                            document.addEventListener('click', enableAudio, { once: true });
                            document.addEventListener('touchstart', enableAudio, { once: true });
                            document.addEventListener('keydown', enableAudio, { once: true });
                            
                        }).catch(err => {
                            console.error('静音自动播放仍然失败:', err);
                        });
                    });
                }
            } catch (e) {
                console.error('播放尝试失败', e);
            }
        };
        
        // 多种方法尝试自动播放
        
        // 1. 即刻尝试播放（DOMContentLoaded时已调用）
        attemptPlayMethods();
        
        // 2. 连续多次尝试，覆盖不同时间点
        for (let i = 1; i <= 5; i++) {
            setTimeout(attemptPlayMethods, i * 1000);
        }
        
        // 3. 页面完全加载后尝试
        window.addEventListener('load', attemptPlayMethods);
        
        // 4. 监听页面状态变化
        if ('hidden' in document) {
            document.addEventListener('visibilitychange', function() {
                if (!document.hidden && (backgroundMusic.paused || backgroundMusic.muted)) {
                    attemptPlayMethods();
                }
            });
        }
        
        // 5. 监听用户交互以解锁音频
        const userInteractionEvents = ['click', 'touchstart', 'touchend', 'mousedown', 'keydown', 'scroll'];
        
        const handleUserInteraction = () => {
            if (backgroundMusic.paused || backgroundMusic.muted) {
                backgroundMusic.muted = false;
                backgroundMusic.play()
                    .then(() => {
                        console.log('用户交互后成功播放音乐');
                        updatePlayerState(true);
                    })
                    .catch(e => console.error('用户交互后播放失败', e));
            }
            
            // 只在第一次交互时解除所有事件监听
            userInteractionEvents.forEach(event => {
                document.removeEventListener(event, handleUserInteraction);
            });
        };
        
        // 添加所有交互监听器
        userInteractionEvents.forEach(event => {
            document.addEventListener(event, handleUserInteraction);
        });
        
        // 点击音乐控制按钮
        musicToggle.addEventListener('click', function(e) {
            e.stopPropagation(); // 阻止事件冒泡
            
            if (backgroundMusic.paused) {
                // 如果暂停状态，开始播放
                backgroundMusic.muted = false;
                backgroundMusic.play()
                    .then(() => {
                        updatePlayerState(true);
                    })
                    .catch(error => {
                        console.error('播放音乐失败:', error);
                    });
            } else {
                if (backgroundMusic.muted) {
                    // 如果静音状态，取消静音
                    backgroundMusic.muted = false;
                    updatePlayerState(true);
                } else {
                    // 如果正常播放状态，暂停播放
                    backgroundMusic.pause();
                    updatePlayerState(false);
                }
            }
        });
        
        // 监听音频播放状态变化
        backgroundMusic.addEventListener('play', () => {
            updatePlayerState(true);
        });
        
        backgroundMusic.addEventListener('pause', () => {
            updatePlayerState(false);
        });
    }
